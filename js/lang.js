$(document).ready(function () {
    // 1. Cek local storage, default ke 'id'
    let currentLang = localStorage.getItem('language') || 'id';

    // 2. Fungsi update text
    function updateContent(lang) {
        // Update tombol aktif
        $('.lang-btn').removeClass('active');
        $(`.lang-btn[data-lang="${lang}"]`).addClass('active');

        // Loop semua elemen dengan atribut data-i18n
        $('[data-i18n]').each(function () {
            const key = $(this).data('i18n');
            if (resources[lang][key]) {
                // Gunakan .html() agar tag <br> atau <p> ikut ter-render
                $(this).html(resources[lang][key]);
            }
        });
    }

    // 3. Jalankan saat pertama load
    updateContent(currentLang);

    // 4. Event listener saat tombol diklik
    $('.lang-btn').on('click', function (e) {
        e.preventDefault();
        const selectedLang = $(this).data('lang');
        localStorage.setItem('language', selectedLang);
        updateContent(selectedLang);
    });
});
